

class Customer:
    def __init__(self,name,wallet,age,drunkeness):
        self.name = name
        self.wallet = wallet
        self.age = age
        self.drunkeness = drunkeness
       
        
    
    def decrease_wallet(self,cash):
        self.wallet -= cash
        
    def increase_drunkness(self,drink):
        self.drunkeness += drink.alcohol_level
        

        

        
    
        
        
        
        
        
        
        
        
        
        
    
    
    
        
        